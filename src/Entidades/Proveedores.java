    public void getTodos() {
        li = new ArrayList();
        try {
            DriverManager.registerDriver(new org.gjt.mm.mysql.Driver());
            try (
                    Connection conexion = DriverManager.getConnection("jdbc:mysql://localhost/jhondee", "root", "1234")) {
                PreparedStatement s = conexion.prepareStatement("SELECT rif,nombre,direccion FROM jhondee."+tabla+" where estatus = 'a'");
                ResultSet rs = s.executeQuery();
                // Se recorre el ResultSet, mostrando por pantalla los resultados.
                while (rs.next()) {
                    li.add(new Modelos.Entidades(rs.getString("rif"), rs.getString("nombre"), rs.getString("direccion")));
                }
                // Se cierra la conexi√≥n con la base de datos.
            }
        } catch (SQLException e) {
        }
    }

    void settabla() {
        tablaClientes te;
        te = new tablaClientes();
        te.setalmacen((List<Entidades>) li.clone());
        Entes.setModel(te);
        Entes.getColumnModel().getColumn(0).setPreferredWidth(750);
        Entes.getColumnModel().getColumn(1).setPreferredWidth(1500);
    }

