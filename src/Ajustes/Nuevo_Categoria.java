    private void insert() {
        {
            try {
                String myDriver = "org.gjt.mm.mysql.Driver";
                String myUrl = "jdbc:mysql://localhost/jhondee";
                Class.forName(myDriver);
                Connection conn = DriverManager.getConnection(myUrl, "root", "1234");
                // the mysql insert statement
                String query = "INSERT INTO `categorias`" +
                                "(`cod`,`nombre`)" +
                                "VALUES(?,?)";
                PreparedStatement preparedStmt = conn.prepareStatement(query);
                preparedStmt.setInt(1, getNcod());
                preparedStmt.setString(2,"--"+ Nombre.getText());
                preparedStmt.execute();
                conn.close();
            } catch (Exception e) {
                System.err.println("Got an exception!");
                System.err.println(e.getMessage());
            }
        }
    }

    private int getcod(String c) {
        int t = 0;
        try {
            DriverManager.registerDriver(new org.gjt.mm.mysql.Driver());
            try (
                    Connection conexion = DriverManager.getConnection("jdbc:mysql://localhost/jhondee", "root", "1234")) {
                PreparedStatement s = conexion.prepareStatement(c);
                ResultSet rs = s.executeQuery();
                // Se recorre el ResultSet, mostrando por pantalla los resultados.
                rs.next();
                t = rs.getInt(1);

                // Se cierra la conexi√≥n con la base de datos.
            }//1000;
        } catch (SQLException e) {
        }
        return t;
    }

    private int getNcod() {
        String query = "select MAX(cod) from categorias ";
                if(Telas.isSelected())query += " where cod < 200000";
                if(Accesorios.isSelected())query += " where cod < 300000";
                if(Consumibles.isSelected())query += " where cod < 400000";
                return getcod(query)+1000;
    }



